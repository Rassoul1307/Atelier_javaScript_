// En utilisant la boucle while(), afficher dans la console
// tous les codes postaux possibles pour le département 77.
let codePostal = 77000
while (codePostal < 78000) {
    // Affichage du résultat de l'opération sur la console avec un retour à la ligne.
    document.write(`${codePostal} <br>`)
    codePostal++
}